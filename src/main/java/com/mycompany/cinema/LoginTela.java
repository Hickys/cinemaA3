    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        
        //pega o login do usuario
        String email = loginTextField.getText();
        //pega a senha
        String senha = new String(senhaPasswordField.getPassword());
        
        //verifica o usuario
        try {
            Usuario usuario = new Usuario(null, null, email, senha,  null);
            DAO dao = new DAO();
            
            if(dao.existe(usuario)){
                JOptionPane.showMessageDialog(null, "deu certo");
            } else {
                JOptionPane.showMessageDialog(null, "deu merda");
        }
        }
        catch(Exception e){
                JOptionPane.showMessageDialog(null, "deu um erro");
                }
    }//GEN-LAST:event_loginButtonActionPerformed

